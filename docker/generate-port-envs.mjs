#!/usr/bin/env node
/**
 * Writes docker/ports.host.*.env from docker/ports.base.json + fixed offsets.
 * Run: node docker/generate-port-envs.mjs   (or npm run docker:gen-ports)
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const basePath = path.join(__dirname, "ports.base.json");

const { base } = JSON.parse(fs.readFileSync(basePath, "utf8"));

/**
 * Public browser / CORS origins. Local profiles use localhost + offset web port;
 * staging and production use real hosts (reverse proxy terminates TLS in front of HOST_PORT_WEB).
 */
function publicAppUrls(profileName, webPort) {
  if (profileName === "staging") {
    return {
      webOrigin: "https://staging.ceremonywallet.com",
      nextPublicAppUrl: "https://staging.ceremonywallet.com",
    };
  }
  if (profileName === "production") {
    return {
      webOrigin: "https://ceremonywallet.com",
      nextPublicAppUrl: "https://ceremonywallet.com",
    };
  }
  const local = `http://localhost:${webPort}`;
  return { webOrigin: local, nextPublicAppUrl: local };
}

/** Host port = base[key] + offset. */
const profiles = [
  { name: "default", offset: 0 },
  { name: "dev", offset: 30 },
  { name: "staging", offset: 40, hostBind: "127.0.0.1" },
  { name: "production", offset: 50, hostBind: "127.0.0.1" },
];

for (const { name, offset, hostBind } of profiles) {
  const p = (k) => base[k] + offset;
  const webPort = p("web");
  const { webOrigin, nextPublicAppUrl } = publicAppUrls(name, webPort);
  const lines = [
    "# Auto-generated by docker/generate-port-envs.mjs — edit docker/generate-port-envs.mjs (URLs) or docker/ports.base.json (ports) then run: npm run docker:gen-ports",
    `# Profile: ${name} (offset +${offset})`,
    "",
    ...(hostBind ? [`HOST_BIND=${hostBind}`, ""] : []),
    `HOST_PORT_MYSQL=${p("mysql")}`,
    `HOST_PORT_MAILPIT_UI=${p("mailpitUi")}`,
    `HOST_PORT_MAILPIT_SMTP=${p("mailpitSmtp")}`,
    `HOST_PORT_GARAGE_S3=${p("garageS3")}`,
    `HOST_PORT_GARAGE_ADMIN=${p("garageAdmin")}`,
    `HOST_PORT_API=${p("api")}`,
    `HOST_PORT_WEB=${webPort}`,
    "",
    "# Used by docker-compose when this file is passed as --env-file (WEB_ORIGIN / NEXT_PUBLIC_APP_URL for API + web)",
    `WEB_ORIGIN=${webOrigin}`,
    `NEXT_PUBLIC_APP_URL=${nextPublicAppUrl}`,
    "",
  ];
  const body = lines.join("\n");

  const out = path.join(__dirname, `ports.host.${name}.env`);
  fs.writeFileSync(out, body, "utf8");
  console.log("wrote", path.relative(root, out));
}
