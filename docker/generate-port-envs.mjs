#!/usr/bin/env node
/**
 * Writes docker/ports.host.*.env from docker/ports.base.json + fixed offsets.
 * Run: node docker/generate-port-envs.mjs   (or npm run docker:gen-ports)
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const basePath = path.join(__dirname, "ports.base.json");

const { base } = JSON.parse(fs.readFileSync(basePath, "utf8"));

/** Host port = base[key] + offset. Browser URLs use the web port. */
const profiles = [
  { name: "default", offset: 0 },
  { name: "dev", offset: 30 },
  { name: "staging", offset: 40 },
  { name: "production", offset: 50 },
];

for (const { name, offset } of profiles) {
  const p = (k) => base[k] + offset;
  const webPort = p("web");
  const lines = [
    "# Auto-generated by docker/generate-port-envs.mjs — edit docker/ports.base.json then run: npm run docker:gen-ports",
    `# Profile: ${name} (offset +${offset})`,
    "",
    `HOST_PORT_MYSQL=${p("mysql")}`,
    `HOST_PORT_MAILPIT_UI=${p("mailpitUi")}`,
    `HOST_PORT_MAILPIT_SMTP=${p("mailpitSmtp")}`,
    `HOST_PORT_GARAGE_S3=${p("garageS3")}`,
    `HOST_PORT_GARAGE_ADMIN=${p("garageAdmin")}`,
    `HOST_PORT_API=${p("api")}`,
    `HOST_PORT_WEB=${webPort}`,
    "",
    "# Used by docker-compose for API/web defaults when this file is passed as --env-file",
    `WEB_ORIGIN=http://localhost:${webPort}`,
    `NEXT_PUBLIC_APP_URL=http://localhost:${webPort}`,
    "",
  ];
  const body = lines.join("\n");

  const out = path.join(__dirname, `ports.host.${name}.env`);
  fs.writeFileSync(out, body, "utf8");
  console.log("wrote", path.relative(root, out));
}
